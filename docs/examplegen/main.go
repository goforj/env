const generatedMarker = "// Code generated by docs/examplegen; DO NOT EDIT."

// main renders the reproducible documentation artifacts for this module.
func main() {
	if err := run(); err != nil {
		fmt.Println("Error:", err)
		os.Exit(1)
	}
	fmt.Println("✔ Examples generated in ./examples/")
}

// run regenerates the exact set of documented examples and removes only stale generated entrypoints.
func run() error {
	root, err := findRoot()
	if err != nil {
		return err
	}

	examplesDir := filepath.Join(root, "examples")
	if err := os.MkdirAll(examplesDir, 0o755); err != nil {
		return err
	}

	modPath, err := modulePath(root)
	if err != nil {
		return err
	}

	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, root, nil, parser.ParseComments)
	if err != nil {
		return err
	}

	pkgName, err := selectPackage(pkgs)
	if err != nil {
		return err
	}

	pkg, ok := pkgs[pkgName]
	if !ok {
		return fmt.Errorf(`package %q not found in %s`, pkgName, root)
	}

	funcs := map[string]*FuncDoc{}

	filenames := make([]string, 0, len(pkg.Files))
	for filename := range pkg.Files {
		filenames = append(filenames, filename)
	}
	sort.Strings(filenames)
	for _, filename := range filenames {
		file := pkg.Files[filename]
		if strings.Contains(filename, "_test.go") {
			continue
		}

		for identity, fd := range extractFuncDocs(fset, filename, file) {
			if existing, ok := funcs[identity]; ok {
				existing.Examples = append(existing.Examples, fd.Examples...)
			} else {
				funcs[identity] = fd
			}
		}
	}

	identities := make([]string, 0, len(funcs))
	nameCounts := make(map[string]int, len(funcs))
	for identity := range funcs {
		identities = append(identities, identity)
		nameCounts[funcs[identity].Name]++
	}
	sort.Strings(identities)
	expectedExamples := make(map[string]struct{}, len(funcs))
	for _, identity := range identities {
		fd := funcs[identity]
		if len(fd.Examples) > 0 {
			expectedExamples[exampleDirectoryName(fd, nameCounts[fd.Name] > 1)] = struct{}{}
		}
	}
	if err := cleanGeneratedExamples(examplesDir, expectedExamples); err != nil {
		return err
	}
	for _, identity := range identities {
		fd := funcs[identity]
		sort.Slice(fd.Examples, func(i, j int) bool {
			return fd.Examples[i].Line < fd.Examples[j].Line
		})

		if err := writeMain(examplesDir, fd, modPath, nameCounts[fd.Name] > 1); err != nil {
			return err
		}

	}

	return nil
}

// findRoot anchors generation to the root module from either the root or docs directory.
func findRoot() (string, error) {
	wd, err := os.Getwd()
	if err != nil {
		return "", fmt.Errorf("get working directory: %w", err)
	}
	if fileExists(filepath.Join(wd, "go.mod")) {
		return wd, nil
	}
	parent := filepath.Join(wd, "..")
	if fileExists(filepath.Join(parent, "go.mod")) {
		return filepath.Clean(parent), nil
	}
	return "", fmt.Errorf("could not find project root")
}

// fileExists lets root discovery ignore candidate paths that are not present.
func fileExists(p string) bool { _, err := os.Stat(p); return err == nil }

// modulePath reads the canonical import prefix so generated code never hard-codes a checkout identity.
func modulePath(root string) (string, error) {
	data, err := os.ReadFile(filepath.Join(root, "go.mod"))
	if err != nil {
		return "", err
	}

	for _, line := range strings.Split(string(data), "\n") {
		line = strings.TrimSpace(line)
		if strings.HasPrefix(line, "module ") {
			return strings.TrimSpace(strings.TrimPrefix(line, "module ")), nil
		}
	}

	return "", fmt.Errorf("module path not found in go.mod")
}

