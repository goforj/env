// Write ./examples/<func>/main.go
// ------------------------------------------------------------
//

// writeMain formats one consumer-facing executable per documented API identity.
func writeMain(base string, fd *FuncDoc, importPath string, nameCollides bool) error {
	if len(fd.Examples) == 0 {
		return nil
	}

	if importPath == "" {
		return fmt.Errorf("import path cannot be empty")
	}

	dir := filepath.Join(base, exampleDirectoryName(fd, nameCollides))
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return err
	}

	var buf bytes.Buffer

	buf.WriteString("//go:build ignore\n")
	buf.WriteString("// +build ignore\n\n")
	buf.WriteString(generatedMarker + "\n\n")

	buf.WriteString("package main\n\n")

	imports := map[string]bool{
		importPath: true,
	}

	for _, ex := range fd.Examples {
		if strings.Contains(ex.Code, "fmt.") {
			imports["fmt"] = true
		}
		if strings.Contains(ex.Code, "strings.") {
			imports["strings"] = true
		}
		if strings.Contains(ex.Code, "os.") {
			imports["os"] = true
		}
		if strings.Contains(ex.Code, "filepath.") {
			imports["path/filepath"] = true
		}
		if strings.Contains(ex.Code, "godump.") {
			imports["github.com/goforj/godump"] = true
		}
	}

	writeImports(&buf, imports)

	buf.WriteString("// main keeps this documented example executable so API drift fails during compilation.\n")
	buf.WriteString("func main() {\n")

	if fd.Description != "" {
		for _, line := range strings.Split(fd.Description, "\n") {
			buf.WriteString("\t// " + line + "\n")
		}
		buf.WriteString("\n")
	}

	for _, ex := range fd.Examples {
		if ex.Label != "" {
			buf.WriteString("\t// Example: " + ex.Label + "\n")
		}

		ex.Code = strings.TrimLeft(ex.Code, "\n")

		for _, line := range strings.Split(ex.Code, "\n") {
			if strings.TrimSpace(line) == "" {
				buf.WriteString("\n")
			} else {
				buf.WriteString("\t" + line + "\n")
			}
		}
	}

	buf.WriteString("}\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("format example %s: %w", funcIdentity(fd.Receiver, fd.Name), err)
	}
	return os.WriteFile(filepath.Join(dir, "main.go"), formatted, 0o644)
}

// cleanGeneratedExamples removes obsolete generated entrypoints while preserving manual examples.
func cleanGeneratedExamples(base string, expected map[string]struct{}) error {
	entries, err := os.ReadDir(base)
	if err != nil {
		return fmt.Errorf("read examples directory: %w", err)
	}
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		if _, keep := expected[entry.Name()]; keep {
			continue
		}
		path := filepath.Join(base, entry.Name(), "main.go")
		contents, err := os.ReadFile(path)
		if errors.Is(err, os.ErrNotExist) {
			continue
		}
		if err != nil {
			return fmt.Errorf("read generated example %s: %w", path, err)
		}
		if !bytes.Contains(contents, []byte(generatedMarker)) {
			continue
		}
		if err := os.Remove(path); err != nil {
			return fmt.Errorf("remove obsolete generated example %s: %w", path, err)
		}
	}
	return nil
}

// exampleDirectoryName qualifies colliding methods without renaming established unique examples.
func exampleDirectoryName(fd *FuncDoc, nameCollides bool) string {
	if nameCollides && fd.Receiver != "" {
		return strings.ToLower(fd.Receiver + "-" + fd.Name)
	}
	return strings.ToLower(fd.Name)
}

// writeImports renders stable standard-library and third-party import groups.
func writeImports(buf *bytes.Buffer, imports map[string]bool) {
	if len(imports) == 1 {
		for path := range imports {
			fmt.Fprintf(buf, "import %q\n\n", path)
		}
		return
	}

	standard := make([]string, 0, len(imports))
	thirdParty := make([]string, 0, len(imports))
	for path := range imports {
		firstSegment := strings.Split(path, "/")[0]
		if strings.Contains(firstSegment, ".") {
			thirdParty = append(thirdParty, path)
			continue
		}
		standard = append(standard, path)
	}
	sort.Strings(standard)
	sort.Strings(thirdParty)

	buf.WriteString("import (\n")
	for _, path := range standard {
		fmt.Fprintf(buf, "\t%q\n", path)
	}
	if len(standard) > 0 && len(thirdParty) > 0 {
		buf.WriteString("\n")
	}
	for _, path := range thirdParty {
		fmt.Fprintf(buf, "\t%q\n", path)
	}
	buf.WriteString(")\n\n")
}
